    print(f"   - Contaminación (Outliers): {entrada['contaminacion']:.1%}")
    print(f"\n📤 RESULTADO DEL PROCESAMIENTO:")
    print(f"   - Registros finales (limpios): {X_res.shape[0]}")
    print(f"   - Registros eliminados por anomalía: {entrada['df'].shape[0] - X_res.shape[0]}")
    print(f"   - Total de features (tras OneHot): {X_res.shape[1]}")

    if coinciden:
        print("\n✅ VEREDICTO: ¡PRUEBA PASADA! Los datos están correctamente preprocesados.")
    else:
        print("\n❌ VEREDICTO: ERROR. Los datos procesados no coinciden con el estándar.")
    print("="*65)